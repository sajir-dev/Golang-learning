package main

import "fmt"

func main() {
	num1 := 23
	num2 := 34

	fmt.Println("equality operator: ", num1 == num2)
	fmt.Println("less than or equal to: ", num1 <= num2)
	fmt.Println("greater than or equal to: ", num1 >= num2)
	fmt.Println("not equal to: ", num1 != num2)
	fmt.Println("less than: ", num1 < num2)
	fmt.Println("greater than: ", num1 > num2)
}
